score = 98
high_score = 100

print(score == high_score) # equals, evaluate to False
print(score != high_score) # not equals, evaluate to True
print(score < high_score) # less than, True
print(score <= high_score) # less than or equal, True
print(score > high_score) # greater than, False
print(score >= high_score) # greater than or equal, False
