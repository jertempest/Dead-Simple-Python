foo = 10
print(foo)
foo += 10 # value is now 20 (10 + 10)
print(foo)
foo -= 5 #value is now 15 (20 - 5)
print(foo)
foo *= 16 # value is now 240 (15 * 16)
print(foo)
foo //= 5 #value is now 48 (240 // 5)
print(foo)
foo /= 4 # value is 12.0 (48 / 4)
print(foo)
foo **= 2 #value is 144.0 (12 ** 2)
print(foo)
foo %= 51 # value is 42.0 (144.0 % 51)
print(foo)
