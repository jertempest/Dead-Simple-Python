print(9 & 8) #bitwise AND, evaluates to 8 (1000 & 1001)
print(9 | 8) #bitwise OR, evaluates to 9  (1000 | 1001) 
print(9 ^ 8) #bitwise XOR, evaluates to 1 (1000 ^ 1001) 
print(~8) #unary bitwise ones complement (flip), evaluates -9
print(1 << 3) # bitwise left shift, evaluates to 8 (0001 = 1000)
print(8 >> 3) #bitwise right shift, evaluates to 1 (1000 = 0001)
